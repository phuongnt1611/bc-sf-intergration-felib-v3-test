	public void selectItemInCustomDropdown(WebDriver driver, String locatorTypeParent, String locatorTypeAllItems,
			String textExpected) {
		getWebElement(driver, locatorTypeParent).click();

		WebDriverWait explicitWait = new WebDriverWait(driver, longTimeout);

		explicitWait.until(ExpectedConditions.presenceOfAllElementsLocatedBy(getByLocator(locatorTypeAllItems)));

		List<WebElement> listAllElement = driver.findElements(By.xpath(locatorTypeAllItems));
		for (WebElement itemElement : listAllElement) {
			if (itemElement.getText().trim().equals(textExpected)) {
				explicitWait.until(ExpectedConditions.elementToBeClickable(itemElement));
				sleepInSecond(2);
				itemElement.click();
				sleepInSecond(2);
				break;
			}
		}

	}

